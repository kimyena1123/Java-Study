package fastcampus.java.Course1.part2;

public class FourArithmetic {
    public static void main(String[] args) {
        int a = 12;
        int b = 2;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
    }
}
