package fastcampus.java.Course1.part2;

public class AssignOperTest {
    public static void main(String[] args) {
        int num = 1;

        num++; //2
        System.out.println(num);

        ++num; //3
        System.out.println(num);

        num = num + 1; //4
        System.out.println(num);

        num += 1; //5
        System.out.println(num);
    }
}
